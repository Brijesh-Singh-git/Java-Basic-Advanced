public class PostiveNegativeNumber {
    public static void main(String[] args) {

        int num = -4;

        if (num < 0) {
            System.out.println("Negative Number");
        } else if (num == 0) {
            System.out.println("Equals to Zero");
        } else {
            System.out.println("Positive Number");
        }
    }
}
